/**
 * Heatmap cell style configuration
 */
export interface HeatmapCell {
  /**
   * Cell border color
   */
  stroke?: string | null
  /**
   * Cell border width
   */
  lineWidth?: number | null
  /**
   * Cell corner radius
   */
  cornerRadius?: number | null
  /**
   * Hover shadow color
   */
  hoverShadowColor?: string | null
}
